    private void popuniTipovePeciva() {
        try {
            ArrayList<TipPeciva> tipoviPeciva = ClientController.getInstance().getAllTipPeciva();

            cmbTipPeciva.removeAllItems();

            for (TipPeciva tipPeciva : tipoviPeciva) {
                cmbTipPeciva.addItem(tipPeciva);
            }

        } catch (Exception ex) {
            Logger.getLogger(NovoPecivoForma.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

