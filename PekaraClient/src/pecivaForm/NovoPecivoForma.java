    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
        try {
            if (txtNazivPeciva.getText().isEmpty() || txtCenaPoKomadu.getText().isEmpty()
                    || txtOpis.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Sva polja moraju biti popunjena!");
                return;
            }

            TipPeciva tipPeciva = (TipPeciva) cmbTipPeciva.getSelectedItem();
            String nazivPeciva = txtNazivPeciva.getText();
            double cenaPoKomadu = Double.parseDouble(txtCenaPoKomadu.getText());
            String opis = txtOpis.getText();

            TableModelSastojci tm = (TableModelSastojci) tblSastojci.getModel();

            Pecivo p = new Pecivo(null, nazivPeciva, cenaPoKomadu, opis, tipPeciva, tm.getLista());

            ClientController.getInstance().addPecivo(p);
          
            JOptionPane.showMessageDialog(this, "Uspesno dodato pecivo.");
            this.dispose();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Veza sa serverom prekinuta");
        }
    }//GEN-LAST:event_btnDodajActionPerformed

